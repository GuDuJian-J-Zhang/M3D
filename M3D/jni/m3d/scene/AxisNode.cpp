void AxisNode::SetViewSize(int width, int height, float ratio)
{
	//在横竖屏切换时,将坐标轴位置设置标志设置为空，在绘制坐标轴时强制更新显示数据。
	m_position = AxisNode::NONE;

	//坐标轴宽和高
	this->m_iWidth = width * ratio;
	this->m_iHeight = height * ratio;

	//让坐标轴靠近边缘显示，距离以宽高的较小值为准
	if(this->m_iWidth > this->m_iHeight)
	{
		this->m_iWidth = this->m_iHeight;
	}else
	{
		this->m_iHeight = this->m_iWidth;
	}

	//纪录当前屏幕的宽度和高度
	 m_srcWidth = width;
	 m_srcHeigh = height;

	 //坐标轴显示高度
	this->m_iW =
			this->m_iWidth>= this->m_iHeight ? this->m_iHeight : this->m_iWidth;
}

