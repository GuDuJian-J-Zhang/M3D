void AxisNode::FindVisiableObject(RenderAction* renderAction)
{
	if(this->IsHide()){
		renderAction->SetAxisNode(NULL);
	}else{
		this->SetAxisPosition(SVIEW::Parameters::Instance()->m_axisPos);

		CameraNode * camera = renderAction->GetCamera();
		Matrix3 mat = camera->GetRotation().RotationMatrix().Inverse();
		Quaternion rotateQuat(mat);
		this->SetRotation(rotateQuat);
		renderAction->SetAxisNode(this);

	}
}

void AxisNode::SetAxisPosition(int position)
{
	if(m_position != position)
	{
		m_position = position;

		int screenWidth = m_srcWidth;
		int screenHeigh = m_srcHeigh;

	    if (position == AxisNode::LEFTTOP)
		{
	    	m_iViewX = 0;
	    	m_iViewY = screenHeigh - m_iHeight;
		}
		else if (position == AxisNode::LEFTDOWN)
		{
			m_iViewX = 0;
			m_iViewY = 0;
		}
		else if (position == AxisNode::RIGHTTOP)
		{
			m_iViewX = screenWidth - this->m_iWidth;
			m_iViewY = screenHeigh - m_iHeight;
		}
		else if (position == AxisNode::RIGHTDOWN)
		{
			m_iViewX = screenWidth -this->m_iWidth;
			m_iViewY = 0;
		}else
		{
			m_iViewX = 0;
			m_iViewY = 0;
		}
	}
}
